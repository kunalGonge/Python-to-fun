#In this we will see more about arithmetic operator
num1 = int(input ('what is num1?'))
num2 = int(input ('what is num2?'))

#for add +
print(num1 + num2)
#for sub -
print(num1 - num2)
#for multiply *
print(num1 * num2)
#for divide /
print(num1 / num2)
#for divide // but this give only unit(non-decimal) value
print(num1 // num2)
#for reminder %
print(num1 % num2)
#for powar of any num
print(num1 ** num2)